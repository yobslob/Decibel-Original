import mongoose from "mongoose";

const dmSchema = new mongoose.Schema({
    senderId: {
        type: mongoose.Schema.Types.ObjectId,
        ref: "User",
        required: true
    },
    receiverId: {
        type: mongoose.Schema.Types.ObjectId,
        ref: "User",
        required: true
    },
    message: {
        type: String,
        required: true
    },

}, { timestamps: true });

const Dm = mongoose.model("Dm", dmSchema);
export default Dm;